<?php 
header('content-type:text/html;charset=utf-8');


//一维数组

$arr=[
    0=>'熊妈妈',
    1=>'熊爸爸',
    2=>'熊宝宝',
]; //普通的  一维数组


//二维数组  (一维数组的每个单元又保存了另一个数组,这就叫二维数组)

$arr1=[
    0=>['熊妈妈','熊奶奶'],
    1=>['熊爸爸','熊爷爷'],
    3=>['熊宝宝','小熊熊'],
];
echo $arr1[0][1].'<br>'; //取其中一个值


//去二维数组里的每个数

foreach($arr1 as $value){
    //第一次循环 $value =['熊妈妈','熊奶奶'];
    //第二次循环 $value=['熊爸爸','熊爷爷'];
    //第三次循环 $value=['熊宝宝','小熊熊'];
    echo $value[0].$value[1].'<br>';
}



?>